package operators;

public class SDET062_UnaryOperatorDemo {
public static void main(String[] args) {
	// Unary
    int a1 = +10;
    int a2 = -10;
    System.out.println(a1);
    System.out.println(a2);
    
    int age = 65; // unary plus
    int num = -1; // unary plus - only 1 operand
    System.out.println(age+num);
}
    
}

